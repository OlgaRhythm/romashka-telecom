package com.romashka.romashka_telecom.brt.enums;

/**
 * Тип сети для звонка.
 * Определяет, является ли звонок внутренним (между абонентами Ромашки)
 * или внешним (на номера других операторов).
 */
public enum NetworkType {
    /** Звонок между абонентами Ромашки */
    INTERNAL,    // внутри сети
    /** Звонок на номера других операторов */
    EXTERNAL     // на другую сеть
} 